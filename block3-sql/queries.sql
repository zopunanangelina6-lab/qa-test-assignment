-- ============================================================================
-- ЗАДАЧА 1: Вторая по величине уникальная зарплата
-- ============================================================================
-- Дана таблица: Employee(id int PK, salary int)
-- Найти вторую по величине уникальную зарплату.
-- Если второй зарплаты не существует — вернуть NULL.

-- ВАРИАНТ 1: Через подзапрос с MAX (рекомендуемый)
-- Преимущество: гарантированно возвращает NULL, если второй зарплаты нет
SELECT MAX(salary) AS second_highest_salary
FROM Employee
WHERE salary < (SELECT MAX(salary) FROM Employee);

-- ВАРИАНТ 2: Через DISTINCT + LIMIT/OFFSET
-- Недостаток: если второй зарплаты нет, вернёт пустой результат (не NULL)
SELECT DISTINCT salary
FROM Employee
ORDER BY salary DESC
LIMIT 1 OFFSET 1;

-- ВАРИАНТ 3: Через оконную функцию DENSE_RANK()
-- Преимущество: масштабируется на N-ю зарплату
SELECT salary
FROM (
    SELECT salary, DENSE_RANK() OVER (ORDER BY salary DESC) AS rnk
    FROM Employee
) ranked
WHERE rnk = 2
LIMIT 1;

-- ПОЧЕМУ Я ВЫБРАЛА ВАРИАНТ 1:
-- Он единственный корректно возвращает NULL (а не пустой результат),
-- что соответствует требованию задачи. Также он проще для чтения.


-- ============================================================================
-- ЗАДАЧА 2: Поиск дублирующихся email
-- ============================================================================
-- Дана таблица: Person(id int PK, email varchar)
-- Найти все дублирующиеся email-адреса.

-- ОСНОВНОЕ РЕШЕНИЕ: GROUP BY + HAVING
SELECT email, COUNT(*) AS duplicate_count
FROM Person
GROUP BY email
HAVING COUNT(*) > 1;

